    <p><strong>Reduce repeated test practice in Bangladesh<br /></strong></p>
    <p>General or non-medical persons typically don’t have much knowledge on diagnostic tests. Many of us nowadays are facing repetitive test problems in our country. Unnecessary testing can make up a large proportion of a laboratory’s workload. Acro Medical is working to identify the cause of this problem to try and reduce the rate of repetition without reason. Unnecessary testing is happening due to:</p>
    <ul class="unordered-list"  style="margin-left:32px">
        <li>Not storing reports properly</li>
        <li>Unaware that a test has been previously performed</li>
        <li>Poor understanding of further importance of this test result in future</li>
        <li>Lack of knowledge</li>
        <li>Physicians often suggesting repeat testing</li>
        <li>Fear of errors</li>
        <li>Patients actively ask for tests</li>
    </ul><br>
    <p>All of the above situations are caused by lack of proper documentation and leading to financial drawback. Repeat testing will be reduced by the use of computerized documentation process, general awareness etc. We are working with diagnostic centers that provide repeated test results without associated extra costs if the results are an error caused by the physician. We are keeping records of our members test results in our system and can provide copies of reporting.</p>
